import collections

# Complete the countTriplets function below.
def countTriplets(arr, r):
    v3 = collections.defaultdict(int)
    v2 = collections.defaultdict(int)
    count = 0
    for k in arr:
        count += v3[k]
        v3[k*r] += v2[k]
        v2[k*r] += 1
    return count

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    nr = input().rstrip().split()

